// Scaled9x15 returns scaled Face9x15, no smoothing
func Scaled9x15(scale int) *basicfont.Face {
	pix := []byte{}
	pixString := []byte{}
	for i := 0; i < len(mask9x15.Pix); i++ {
		for j := 0; j < scale; j++ {
			pixString = append(pixString, mask9x15.Pix[i])
		}
		if i%Face9x15.Width == (Face9x15.Width - 1) {
			for j := 0; j < scale; j++ {
				pix = append(pix, pixString...)
			}
			pixString = []byte{}
		}
	}
	rect := image.Rect(0, 0, mask9x15.Rect.Max.X*scale, mask9x15.Rect.Max.Y*scale)
	var scaledMask = &image.Alpha{
		Stride: mask9x15.Stride * scale,
		Rect:   rect,
		Pix:    pix,
	}
	return &basicfont.Face{
		Advance: Face9x15.Advance * scale,
		Width:   Face9x15.Width * scale,
		Height:  Face9x15.Height * scale,
		Ascent:  Face9x15.Ascent * scale,
		Descent: Face9x15.Descent * scale,
		Mask:    scaledMask,
		Ranges:  Face9x15.Ranges,
	}
}

